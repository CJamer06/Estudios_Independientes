#Operaciones de Asignacion

a = 0 # Deseamos modificar la variable a = 0
print("El valor de la variable 'a' es: ", a)

# Las distintas formas para modificar la variable antes definida es por medio de:

a += 5 # Seria igual decir a = a + 5, esto es llamado la suma en asignacion
print("El nuevo valor de la variable 'a' es (a + 5): ", a)

a -= 2 # Seria igual decir a = a - 2, esto es llamado la resta en asignacion
print("El nuevo valor de la variable 'a' es (a - 2): ", a)

a *= 3 # Seria igual decir a = a * 3, esto es llamado la multiplicacion en asignacion
print("El nuevo valor de la variable 'a' es (a * 3): ", a)

a /= 3 # Seria igual decir a = a / 3, esto es llamado la divicion en asignacion
print("El nuevo valor de la variable 'a' es (a / 3): ", a)

a **= 2 # Seria igual decir a = a ** 2 esto es llamado la potencia en asignacion
print("El nuevo valor de la variable 'a' es (aÂ²): ", a)

a %= 2 # Seria igual decir a = a % 2, esto es llamado modulo en asignacion
print("El nuevo valor de la variable 'a' es (a%2): ", a)
