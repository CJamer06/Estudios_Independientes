#Tipos de Datos (Numericos, Cadena, Booleanos)

#Las variables entienden de forma automatica de que tipo son

print("El numero es: ") # print() sirve para mostrar mensajes por consola

numero = 10 #Tipo - int -.
print(numero) # print() sirve para llamar variables.
print(type(numero)) #Esto me mostrara el tipo de variable y nos ayuda a saber que variable tenemos.

numero = 10.56 #Las variables son reutilizabes y se conoce como tipado dinamico (Tipo - float -).
print("El numero es: ")
print(numero) # print() sirve para llamar variables.
print(type(numero)) #Esto me mostrara el tipo de variable y nos ayuda a saber que variable tenemos.

print("La cadena es: ")
cadena = 'Hola' #Para almacenar cadenas ponemos comillas simples -''- o comillas dobles -""-(Tipo - char -).
print(cadena) # print() sirve para llamar variables.
print(type(cadena)) #Esto me mostrara el tipo de variable y nos ayuda a saber que variable tenemos.

print("Verdadero o falso: ")
Valor = True #Almacena datos de tipo Booleano (Verdadero o Falso).
print(Valor) # print() sirve para llamar variables.
print(type(Valor)) #Esto me mostrara el tipo de variable y nos ayuda a saber que variable tenemos.

